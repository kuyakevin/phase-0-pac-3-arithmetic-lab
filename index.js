function add(a, b) {
    return a + b;
    console.log(add(a, b));
}

function subtract(a, b) {
    return a - b;
    console.log(subtract(a, b));
}

function multiply(a, b) {
    return a * b; 
    console.log(multiply(a, b));
}

function divide(a, b) {
    return a / b;
    console.log(divide(a, b));
}

function increment(n) {
    n++
    return (n);
}

function decrement(n) {
    n--
    return (n);
}

function makeInt(n) {
    return parseInt(n, 10);
}

function preserveDecimal(n) {
    return parseFloat(n);
}